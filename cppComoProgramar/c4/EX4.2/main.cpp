#include <iostream>

using std::cout;
using std::cin;
using std::endl;

int main()
{

    int x = 0, y = 0, z = 0, w = 0;

    x = x + 1;
    cout << "x = x + 1: " << x << endl;

    y += 1;
    cout << "x += 1: " << y << endl;

    ++z;
    cout << "++x: " << z << endl;

    w++;
    cout << "x++: " << w << endl;

    return 0;
}
